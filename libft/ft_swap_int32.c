#include "libft.h"

int32_t ft_swap_int32(int32_t value)
{
    value = (((value << 8) & 0xFF00FF00) | ((value >> 8) & 0xFF00FF)); 
    return ((value << 16) | ((value >> 16) & 0xFFFF));
}